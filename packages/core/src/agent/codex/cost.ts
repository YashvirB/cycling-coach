/**
 * Best-effort per-million pricing. Returns undefined on a table miss so the
 * ledger records `cost: undefined` rather than a fabricated zero — preserving
 * the behavior where an uncatalogued model produced no cost. The per-dimension
 * division by 1e6 and the summed total mirror the upstream formula exactly.
 */
export function priceUsage(
  provider: string,
  modelId: string,
  usage: PriceUsageTokens,
): PricedCost | undefined {
  const cost = PRICE_TABLE[provider]?.[modelId];
  if (!cost) return undefined;
  const input = (cost.input / 1_000_000) * usage.input;
  const output = (cost.output / 1_000_000) * usage.output;
  const cacheRead = (cost.cacheRead / 1_000_000) * usage.cacheRead;
  const cacheWrite = (cost.cacheWrite / 1_000_000) * usage.cacheWrite;
  return {
    input,
    output,
    cacheRead,
    cacheWrite,
    total: input + output + cacheRead + cacheWrite,
  };
}

/** True when provider/model is in the price catalog (lets callers skip work). */
export function isPriced(provider: string, modelId: string): boolean {
  return PRICE_TABLE[provider]?.[modelId] !== undefined;
}
